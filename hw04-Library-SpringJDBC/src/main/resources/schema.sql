CREATE TABLE AUTHOR (
    id IDENTITY NOT NULL PRIMARY KEY,
    name VARCHAR(128) NOT NULL
);

CREATE TABLE GENRE (
    id IDENTITY NOT NULL PRIMARY KEY,
    name VARCHAR(128) NOT NULL
);

CREATE TABLE BOOKS (
    id IDENTITY NOT NULL PRIMARY KEY,
    title VARCHAR(128) NOT NULL,
    GENRE_name VARCHAR(128) NOT NULL,
    FOREIGN KEY (GENRE_name) REFERENCES GENRE (name)
);

